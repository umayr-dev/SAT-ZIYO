export async function POST(request: NextRequest) {
  try {
    const body = await request.json();
    const { rwModule1, rwModule2, mathModule1, mathModule2 } = body;

    // Validate inputs
    if (
      typeof rwModule1 !== "number" ||
      typeof rwModule2 !== "number" ||
      typeof mathModule1 !== "number" ||
      typeof mathModule2 !== "number"
    ) {
      return NextResponse.json({ error: "Invalid input" }, { status: 400 });
    }

    // Calculate Digital SAT scores using 2D lookup table (from Excel file)
    // Reading & Writing: 2D lookup table (M1 va M2 alohida indekslar, max 27)
    const readingWritingScore = getScoreFrom2DTable(
      digitalSATReadingWriting2DTable,
      rwModule1,
      rwModule2
    );
    // Math: 2D lookup table (M1 va M2 alohida indekslar, max 22)
    const mathScore = getScoreFrom2DTableForMath(
      digitalSATMath2DTable,
      mathModule1,
      mathModule2
    );
    const totalScore = readingWritingScore + mathScore;
    const percentile = calculatePercentile(totalScore);

    return NextResponse.json({
      readingWriting: readingWritingScore,
      math: mathScore,
      total: totalScore,
      percentile,
    });
  } catch (error) {
    return NextResponse.json({ error: "Calculation failed" }, { status: 500 });
  }
}
